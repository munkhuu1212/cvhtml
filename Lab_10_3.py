def potential_transform(phi, d0):
    # Потенциал функцыг трансформ хийх функц.
    # phi: Анхны потенциал функц.
    # d0: Эхлэлийн утгыг илэрхийлэх өгөгдөл.

    # Эхлэлийн утгыг хадгалах
    phi_d0 = phi(d0)  # d0-ийн потенциал функцийн утгыг тооцно.

    def phi_prime(d):
        # Трансформ хийгдсэн потенциал функц.
        # Φ'(d) = Φ(d) - Φ(d0) гэсэн томъёогоор тодорхойлогдоно.
        return phi(d) - phi_d0

    return phi_prime  # Φ'(d) функцыг буцаана.


stack = []  # Эхлэлийн хоосон стек үүсгэх
phi = lambda s: len(s)  # Потенциал функц: стек дэх элементүүдийн тоог хэмжих
phi_prime = potential_transform(phi, stack)
# Потенциал функцыг трансформ хийн шинэ функц үүсгэх

stack.append(1)  # Стек дээр 1 нэмэх
stack.append(2)  # Стек дээр 2 нэмэх

print("Original Φ:", phi(stack))  # Потенциал функцийн оригинал утга: 2
print("Transformed Φ':", phi_prime(stack))  # Трансформ хийгдсэн утга: 2